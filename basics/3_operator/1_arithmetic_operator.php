<?php

/**
 * Operator - Arithmetic Operator
 * Cara menggunakan operator aritmatika di PHP
 * Penjelasannya silahkan lihat dalam README.
 *
 * Pro tips: Eksekusi kode ini di console sehingga hasilnya akan terlihat
 */

$a = 10;
$b = 5;

// Penjumlahan
var_dump($a + $b); // 15

// Pengurangan
var_dump($a - $b); // 5

// Perkalian
var_dump($a * $b); // 50

// Pemangkatan
var_dump($a ** $b); // 100000

// Pembagian
var_dump($a / $b); // 2

// Modulus
var_dump($a % $b); // 0

// Modulus bersisa
var_dump(11 % $b); // 1
